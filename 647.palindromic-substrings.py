# @lc code=start
class Solution:
    # Check the solution from 5. Longest Palindromic Substring
    # to understand this solution. This is basically the same but
    # counting all possibilities instead of only keeping the maximum
    def countSubstrings(self, s: str) -> int:
        res = 0

        for i in range(len(s)):
            # odd length
            l, r = i, i
            while l >= 0 and r < len(s) and s[l] == s[r]:
                res += 1
                l -= 1
                r += 1

            # even length
            l, r = i, i + 1
            while l >= 0 and r < len(s) and s[l] == s[r]:
                res += 1 # Count single character case
                l -= 1
                r += 1

        return res
        
